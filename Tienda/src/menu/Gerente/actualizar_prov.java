    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        ama.setEnabled(true);
        ama.setEditable(true);
        apa.setEnabled(true);
        apa.setEditable(true);
        comp.setEditable(true);
        comp.setEnabled(true);
        calle.setEnabled(true);
        calle.setEditable(true);
        col.setEnabled(true);
        col.setEditable(true);
        cp.setEnabled(true);
        cp.setEditable(true);
        rfc.setEnabled(true);
        rfc.setEditable(true);
        mail.setEnabled(true);
        edo.setEditable(true);
        edo.setEnabled(true);
        mail.setEditable(true);
        nombre.setEditable(true);
        nombre.setEnabled(true);
        actualizar.setEnabled(true);
        ResultSet rs = null;
       metodos p = new metodos();
       rs = p.bCliente(cod.getText());
       try{
           if(rs.next()){
               nombre.setText(rs.getString("nombre"));
               apa.setText(rs.getString("apPaterno"));
               ama.setText(rs.getString("apMaterno"));
               rfc.setText(rs.getString("RFC"));
               comp.setText(rs.getString("compañia"));
               calle.setText(rs.getString("calle"));
               col.setText(rs.getString("colonia"));
               cp.setText(rs.getString("cp"));
               edo.setText(rs.getString("estado"));
               mail.setText(rs.getString("email"));
           }
           else{
               JOptionPane.showMessageDialog(null, "No hay info. de este RFC");
           }
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex.getMessage());
       }
    }//GEN-LAST:event_jButton3ActionPerformed

