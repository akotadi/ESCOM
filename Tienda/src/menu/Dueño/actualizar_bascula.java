    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        modelo.setEditable(true);
        modelo.setEnabled(true);
        lnom.setEditable(true);
        lnom.setEnabled(true);
        lnum.setEditable(true);
        lnum.setEnabled(true);
        fecha.setEditable(true);
        fecha.setEnabled(true);
        actualizar.setEnabled(true);
        ResultSet rs = null;
       metodos p = new metodos();
       rs = p.bBascula(num.getText(),nom.getText());
       try{
           if(rs.next()){
               modelo.setText(rs.getString("modelo"));
               fecha.setText(rs.getString("fechaLlegada"));
               lnum.setText(rs.getString("Local_numero"));
               lnom.setText(rs.getString("Local_nombre"));
           }
           else{
               JOptionPane.showMessageDialog(null, "No hay info. de esta ID");
           }
       }catch(Exception ex){
           JOptionPane.showMessageDialog(null, ex.getMessage());
       }
    }//GEN-LAST:event_jButton4ActionPerformed

