    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object[][] dtClien;
        String[] columNames={"idPedido", "FechaRealizacion", "FechaEntrega", "noProductos", "MontoTotal", "RFC"};
        metodos p = new metodos();
        if(idFactura.isSelected())
        {
        String nombre;
        nombre = txtDato.getText();
        dtClien=p.getDatosidFactura(nombre);
        DefaultTableModel Datos=new DefaultTableModel(dtClien,columNames);
        Pedidos.setModel(Datos);
        }
        else if(RFC.isSelected())
        {
          String codigo;
         codigo = txtDato.getText();
        dtClien=p.getDatosPedidoRFC(codigo);  
        DefaultTableModel Datos=new DefaultTableModel(dtClien,columNames);
        Pedidos.setModel(Datos);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

