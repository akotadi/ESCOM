    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String[] columNames={"Codigo", "Nombre", "Cantidad"};
        metodos p = new metodos();
        nombre = txtDato.getText();
        dtClien=p.getDatosidPedido(nombre);
        int i = Integer.valueOf((String) dtClien[0][2]);
        DefaultTableModel Datos=new DefaultTableModel(dtClien,columNames);
        productos.setModel(Datos);
        textProduct.setEnabled(true);
        IngresarProducto.setEnabled(true);
        while(i>0){
            cantidad.addItem(Integer.toString(i));
            i--;
            }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void IngresarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarProductoActionPerformed
        // TODO add your handling code here:
        metodos p = new metodos();
        String r =(String) cantidad.getSelectedItem();
        int i = Integer.valueOf(r);
        r = textProduct.getText();
        String s = (String) local.getSelectedItem();
        p.updatePedido(nombre,i,r,s);
    }//GEN-LAST:event_IngresarProductoActionPerformed

