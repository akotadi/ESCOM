    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Object[][] dtClien;
        String[] columNames={"CURP", "Nombre", "Apellido Paterno", "Apellido Materno", "Telefono", "Email"};
        metodos p = new metodos();
        if(Apa.isSelected())
        {
         String nombre;
         nombre = parametro.getText();
        dtClien=p.getEmpAp(nombre);
        DefaultTableModel Datos=new DefaultTableModel(dtClien,columNames);
        clientes.setModel(Datos);
        }
        else if(CURP.isSelected())
        {
          String codigo;
         codigo = parametro.getText();
        dtClien=p.getEmpCURP(codigo);  
        DefaultTableModel Datos=new DefaultTableModel(dtClien,columNames);
        clientes.setModel(Datos);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

