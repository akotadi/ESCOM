    private void atActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atActionPerformed
        // TODO add your handling code here:
        metodos p = new metodos();
        String c, telefono;
        c = curp.getText();
        telefono = tel.getText();
        if(c == ""){
            JOptionPane.showMessageDialog(null,"Ingresa un CURP primero");
        }
        else{
          p.NuevoTelefono(c, telefono);
          tel.setText("");  
        }
    }//GEN-LAST:event_atActionPerformed

