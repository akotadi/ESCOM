    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        metodos p = new metodos();
        String curp1,nomb,app,apm,date,call,col,codigo,edo,tur,sueld,tipo,contra;
        tipo = "";
        curp1 = curp.getText();
        nomb = nombre.getText();
        app = apa.getText();
        apm = ama.getText();
        date =  fecha.getText();
        call = calle.getText();
        col = colonia.getText();
        codigo = cp.getText();
        edo = estado.getText();
        tur =(String) turno.getSelectedItem();
        sueld = sueldo.getText();
        contra = contraseña.getText();
        if(Gerente.isSelected())
        {
            String f;
            tipo = "G";
            f = fechaasig.getText();
            p.NuevoEmpleado(curp1,nomb,app,apm,call,col,codigo,edo,date,tur,sueld,tipo,contra);
            p.NuevoGerente(curp1,f);
        }
        else if(Mostrador.isSelected())
        {
            String lnum,lnom,monto;
            tipo = "M";
            monto = "0";
            lnum = num.getText();
            lnom = nom.getText();
            p.NuevoEmpleado(curp1,nomb,app,apm,call,col,codigo,edo,date,tur,sueld,tipo,contra);
            p.NuevoMostrador(curp1,lnum,lnom,monto);
        }
        tel.setEnabled(true);
        email.setEnabled(true);
        at.setEnabled(true);
        ae.setEnabled(true);
        
    }//GEN-LAST:event_jButton1ActionPerformed

