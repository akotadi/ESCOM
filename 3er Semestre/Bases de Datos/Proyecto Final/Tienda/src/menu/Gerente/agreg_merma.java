    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        Object[][] dtClien;
        String[] columNames={"Codigo","Nombre", "Cantidad"};
        metodos p = new metodos();
        String codigo;
        codigo = cod.getText();
        dtClien=p.getProsimp(codigo);
        DefaultTableModel Datos=new DefaultTableModel(dtClien,columNames);
        productos.setModel(Datos);
        ResultSet st;
        st = p.comboProveedor();
        try{
          while(st.next())
            {
            combo.addItem(st.getString(1));
            }  
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

